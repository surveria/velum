use std::{fs, path::Path};

use anyhow::{Context as _, bail};
use tabled::Table;

use crate::{REPORT_TITLE, RUNNER_NAME, benchmarks, fenced_table, host_benchmark_lock, timing};

pub fn run(report_path: &Path) -> anyhow::Result<()> {
    let report =
        host_benchmark_lock::with_exclusive("project benchmark suite", || Ok(benchmarks::run()))?;
    let body = render(&report);
    if let Some(parent) = report_path.parent() {
        fs::create_dir_all(parent).with_context(|| {
            format!(
                "failed to create benchmark report directory {}",
                parent.display()
            )
        })?;
    }
    fs::write(report_path, body).with_context(|| {
        format!(
            "failed to write benchmark report to {}",
            report_path.display()
        )
    })?;
    println!("benchmark report: {}", report_path.display());
    if report.failed == 0 {
        return Ok(());
    }
    bail!("benchmark run recorded {} failed case(s)", report.failed)
}

fn render(report: &benchmarks::BenchmarkReport) -> String {
    let summary = format!(
        "- Measured: {}\n- In-process measured: {}\n- Failed: {}\n- Invalid: {}\n- Skipped reference: {}\n- Over latency budget ({}): {}\n- Over memory budget ({}): {}\n- Elapsed: {}",
        report.measured,
        report.in_process_measured,
        report.failed,
        report.invalid,
        report.skipped,
        benchmarks::BUDGET_LABEL,
        report.over_latency_budget,
        benchmarks::BUDGET_LABEL,
        report.over_memory_budget,
        timing::format_duration(report.elapsed),
    );
    let table = fenced_table(&Table::new(&report.rows));
    format!(
        "{REPORT_TITLE}\n\nBenchmarks only, generated by {RUNNER_NAME}.\n\n## Benchmarks\n\n{summary}\n\n{table}\n"
    )
}
